/**
 * Simulates an AI response based on user input
 * In a real application, this would be replaced with an API call to a backend
 */
export const getSimulatedResponse = (userInput: string): string => {
  const lowerInput = userInput.toLowerCase();
  
  if (lowerInput.includes("договор") || lowerInput.includes("контракт")) {
    return "Для составления договора мне потребуется информация о сторонах, предмете договора, сроках и условиях. Вы можете предоставить эти детали, и я помогу вам составить проект договора. Также важно учесть применимое законодательство и особенности вашей ситуации.";
  }
  
  if (lowerInput.includes("суд") || lowerInput.includes("иск")) {
    return "Судебные процессы требуют тщательной подготовки. Для подачи иска вам потребуется собрать доказательства, составить исковое заявление и оплатить госпошлину. Сроки исковой давности зависят от типа дела. Могу ли я помочь вам с конкретным вопросом по судебному процессу?";
  }
  
  if (lowerInput.includes("налог") || lowerInput.includes("ндфл")) {
    return "В России действует несколько налоговых режимов. Для физических лиц основная ставка НДФЛ составляет 13%, для нерезидентов - 30%. Существуют различные налоговые вычеты, которые могут уменьшить вашу налоговую базу. Какой конкретно аспект налогообложения вас интересует?";
  }
  
  if (lowerInput.includes("трудов") || lowerInput.includes("работ")) {
    return "Трудовые отношения в России регулируются Трудовым кодексом РФ. Он устанавливает права и обязанности работников и работодателей, порядок заключения и расторжения трудового договора, рабочее время, время отдыха и другие аспекты. Какой конкретный вопрос по трудовому праву вас интересует?";
  }
  
  return "Спасибо за ваш вопрос. Для предоставления точной юридической информации мне нужны дополнительные детали. Могли бы вы уточнить ваш вопрос или предоставить больше контекста? Это поможет мне дать более полезный и точный ответ.";
};